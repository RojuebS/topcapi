Managers = new Class({

    Implements: Options,
    options: {
        id_manage: null
    },

    initialize: function(options) {
        this.setOptions(options);
        this.setElements();
    },

    setElements: function() {
        this.content = new Element('div', {

        }).adopt(
            new Element('h2', {
                'class': 'name',
                'text': managers[this.options.id_manage].name
            }).adopt(
                new Element('div', {
                    'class': 'border-bottom'
                })
            ),

            new Element('p', {
                'class': 'title',
                'text': managers[this.options.id_manage].title
            }),

            new Element('p', {
                'class': 'subtitle',
                'text': managers[this.options.id_manage].subtitle
            })
        );

        new TopCapiBox({
            width: 800,
            height: 490,
            container: this.content
        });
    }
});